					<p align="justify">To Prabhat Joshi being painter is much, much more than a job or a dream to peruse. It has been a lifestyle. A second nature, which has lived with him since childhood. From the crayon patterns on the paper to the marvelous miniature landscapes, with the most minute details. It has been arduous journey. Now he is probably the only scratch painting artist in the World, a style invented and developed by him. </p>
					<p align="justify">Perched in a city 6000 ft. above the sea level in the Shivalik mountain ranges of Mahahimalayas, he is trying to fulfill an almost mystical task of capturing the natural beauty of the region and then translating it into his imaginary paintings called miniature landscapes.</p>
					<p align="justify">Not only this many of his miniatures, reminding of ancient ruins or may be the ruins we all have deep within us, take you to a magical mystery tour in the world, in the past, in your own subconscious. That is probably why he has not given any name to them, you can name them what you like.</p>
					<p align="justify">Prabhat is quite well known for his work, which has won him many accolades. He has organized many exhibitions in the metropolitan districts of India. His work has been praised and appreciated by art lovers and art critics equally. </p>
					<p align="justify">More puzzling is the style of scratch paintings, may be more mysterious then the paintings themselves in which no brush or pellet knife is used initially. Lump of different colors are first applied on the canvas and then given shape by, what he calls them sharp objects. The final touch is given by very thin brush or pellet knife.</p>
					<p align="justify">Prabhat deals in Acrylic, water, ink and oil colors, he hardly uses brushes, and he never opens the secret of his style. The sizes of his miniature paintings range from 2.5 cm x 2.5cm to 5.0 cm x 5.0 cm. The other paintings are larger; he has made large sized oil canvasses too.</p>
